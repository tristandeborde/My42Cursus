#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		sign;
	int		nb;

	sign = 1;
	i = 0;
	nb = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' \
			|| s[i] == '\r' || s[i] == '\f' || s[i] == '\v')
		i++;
	if (s[i] == '+')
		i++;
	else if (s[i] == '-')
	{
		i++;
		sign = -1;
	}
	while (s[i] <= '9' && s[i] >= '0')
	{
		nb *= 10;
		nb += s[i] - '0';
		i++;
	}
	return (nb * sign);
}
