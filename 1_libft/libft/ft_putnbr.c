#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2");
		ft_putnbr(147483648);
	}
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (n < 10)
			ft_putchar(n + 48);
		else
		{
			ft_putnbr((n - (n % 10)) / 10);
			ft_putchar((n % 10) + 48);
		}
	}
}
