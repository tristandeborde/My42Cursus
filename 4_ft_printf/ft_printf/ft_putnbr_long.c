#include "libft.h"

void	ft_putnbr_long(intmax_t n)
{
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n < 10)
		ft_putchar(n + 48);
	else
	{
		ft_putnbr((n - (n % 10)) / 10);
		ft_putchar((n % 10) + 48);
	}
}
