#include <stdlib.h>
#include <string.h>
#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;
	t_list	*tmp2;

	tmp = *alst;
	while (tmp)
	{
		del(tmp->content, tmp->content_size);
		tmp2 = tmp;
		tmp = tmp->next;
		free(tmp2);
	}
	*alst = NULL;
}
